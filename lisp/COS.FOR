      SUBROUTINE UPCASE(BUFF,L)
      INTEGER BUFF(*),L
C                           UPPERCASE THE CHARACTERS IN ABUFF
C                           NUMBER OF CHARACTERS = L
C                           FORMAT IS A1 FORMAT
      DO I = 1,L
        IC = SHIFTR(BUFF(I),56)
        IF (IC.GE.97.AND.IC.LE.122) THEN
          IC = IC - 32
          BUFF(I) = SHIFTL(IC,56).OR.401002004010020040B
        END IF
      END DO
      END
